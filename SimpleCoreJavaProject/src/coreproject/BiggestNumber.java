package coreproject;

public class BiggestNumber {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 40;
		int z = 6;
		
		if (x > y && x > z) 
		{
			System.out.println("The biggest number is 10.");
		}
		else if (y > x && y > z) 
		{
			System.out.println("The biggest number is 40.");
		}
		else 
		{
			System.out.println("The biggest number is 6.");
		}
		

	}

}




